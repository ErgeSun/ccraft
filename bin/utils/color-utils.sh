#!/bin/bash
# color api
function echo_red {
  echo -e "\e[31m$1\e[m"
}

function echo_green {
  echo -e "\e[32m$1\e[m"
}

function echo_yellow {
  echo -e "\e[33m$1\e[m"
}

function echo_dark_blue {
  echo -e "\e[34m$1\e[m"
}

function echo_pink {
  echo -e "\e[35m$1\e[m"
}

function echo_light_blue {
  echo -e "\e[36m$1\e[m"
}
